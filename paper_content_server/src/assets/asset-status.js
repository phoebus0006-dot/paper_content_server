// asset-status.js — Asset lifecycle status definitions and transition rules
// Validates all state transitions before allowing mutation.

var LIFECYCLE_STATUS = {
  DISCOVERED: 'DISCOVERED',
  DOWNLOADED: 'DOWNLOADED',
  VALIDATED: 'VALIDATED',
  SELECTABLE: 'SELECTABLE',
  BLOCKED: 'BLOCKED',
  TOMBSTONED: 'TOMBSTONED',
  DELETED: 'DELETED',
};

var LIBRARY_TYPE = {
  LEARNING: 'LEARNING',
  CUSTOM: 'CUSTOM',
  LEGACY_STUDY: 'LEGACY_STUDY',
  LEGACY_DECORATIVE: 'LEGACY_DECORATIVE',
};

var SAFETY_STATUS = {
  UNKNOWN: 'UNKNOWN',
  PENDING: 'PENDING',
  SAFE: 'SAFE',
  UNSAFE: 'UNSAFE',
  SUSPICIOUS: 'SUSPICIOUS',
};

var RELEVANCE_STATUS = {
  UNKNOWN: 'UNKNOWN',
  RELEVANT: 'RELEVANT',
  IRRELEVANT: 'IRRELEVANT',
};

var QUALITY_STATUS = {
  UNKNOWN: 'UNKNOWN',
  ACCEPTABLE: 'ACCEPTABLE',
  REJECTED: 'REJECTED',
};

var DECODE_STATUS = {
  PENDING: 'PENDING',
  SUCCESS: 'SUCCESS',
  FAILED: 'FAILED',
};

// Allowed lifecycle transitions
var TRANSITIONS = {};
TRANSITIONS[LIFECYCLE_STATUS.DISCOVERED] = [LIFECYCLE_STATUS.DOWNLOADED, LIFECYCLE_STATUS.BLOCKED];
TRANSITIONS[LIFECYCLE_STATUS.DOWNLOADED] = [LIFECYCLE_STATUS.VALIDATED, LIFECYCLE_STATUS.BLOCKED];
TRANSITIONS[LIFECYCLE_STATUS.VALIDATED] = [LIFECYCLE_STATUS.SELECTABLE, LIFECYCLE_STATUS.BLOCKED];
TRANSITIONS[LIFECYCLE_STATUS.SELECTABLE] = [LIFECYCLE_STATUS.BLOCKED];
TRANSITIONS[LIFECYCLE_STATUS.BLOCKED] = [LIFECYCLE_STATUS.TOMBSTONED];
TRANSITIONS[LIFECYCLE_STATUS.TOMBSTONED] = [LIFECYCLE_STATUS.DELETED];
TRANSITIONS[LIFECYCLE_STATUS.DELETED] = [];

var FORBIDDEN = [
  ['UNSAFE', 'SELECTABLE'],
  ['DELETED', 'SELECTABLE'],
  ['TOMBSTONED', 'VALIDATED'],
];

function canTransition(from, to) {
  var allowed = TRANSITIONS[from];
  if (!allowed) return false;
  return allowed.indexOf(to) >= 0;
}

function assertTransition(from, to) {
  if (!canTransition(from, to)) {
    throw new Error('Forbidden transition: ' + from + ' -> ' + to);
  }
}

function canBeSelected(safetyStatus, lifecycleStatus) {
  return safetyStatus === SAFETY_STATUS.SAFE && lifecycleStatus === LIFECYCLE_STATUS.SELECTABLE;
}

module.exports = {
  LIFECYCLE_STATUS: LIFECYCLE_STATUS,
  LIBRARY_TYPE: LIBRARY_TYPE,
  SAFETY_STATUS: SAFETY_STATUS,
  RELEVANCE_STATUS: RELEVANCE_STATUS,
  QUALITY_STATUS: QUALITY_STATUS,
  DECODE_STATUS: DECODE_STATUS,
  canTransition: canTransition,
  assertTransition: assertTransition,
  canBeSelected: canBeSelected,
  FORBIDDEN: FORBIDDEN,
};
